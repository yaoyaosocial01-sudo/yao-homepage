function renderTwin(lang) {
  if (!twinAnswer || !twinPrompts) return;

  const prompts = copy[lang].twinPrompts;
  const activePrompt = prompts.find((prompt) => prompt.id === activeTwinPrompt);

  twinAnswer.textContent = activePrompt?.answer || copy[lang].guideIntros[activeGuide] || copy[lang].twinIntro;
  twinPrompts.innerHTML = "";

  guideButtons.forEach((button) => {
    const isActive = button.dataset.guide === activeGuide;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  prompts.forEach((prompt) => {
    const button = document.createElement("button");
    button.className = "chat-prompt";
    button.type = "button";
    button.textContent = prompt.question;
    button.dataset.promptId = prompt.id;
    button.classList.toggle("is-active", prompt.id === activePrompt?.id);
    button.setAttribute("aria-pressed", String(prompt.id === activePrompt?.id));
    twinPrompts.append(button);
  });
}

function renderNoteForm(lang) {
  const noteCopy = copy[lang].note;

  placeholderNodes.forEach((node) => {
    const key = node.dataset.placeholderKey;
    node.placeholder = noteCopy.placeholders[key] || "";
  });

  if (!noteReason) return;

  const currentValue = noteReason.value;
  noteReason.innerHTML = "";

  noteCopy.reasons.forEach((reason) => {
    const option = document.createElement("option");
    option.value = reason;
    option.textContent = reason;
    noteReason.append(option);
  });

  if (noteCopy.reasons.includes(currentValue)) {
    noteReason.value = currentValue;
  }
}

function setLanguage(lang) {
  document.documentElement.lang = lang === "zh" ? "zh-CN" : "en";
  document.title = copy[lang].pageTitle;
  document.body.dataset.langActive = lang;
  localStorage.setItem("yao-site-language", lang);

  buttons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
    button.setAttribute("aria-pressed", String(button.dataset.lang === lang));
  });

  topicNodes.forEach((node) => {
    const key = node.dataset.topic;
    node.textContent = copy[lang].topics[key];
  });

  topicTitleNodes.forEach((node) => {
    const key = node.dataset.topicTitle;
    node.textContent = copy[lang].topicTitles[key];
  });

  renderTwin(lang);
  renderNoteForm(lang);
}

buttons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

guideButtons.forEach((button) => {
  button.addEventListener("click", () => {
    activeGuide = button.dataset.guide;
    activeTwinPrompt = "intro";
    const lang = document.body.dataset.langActive || "zh";
    twinAnswer.textContent = copy[lang].guideIntros[activeGuide];
    guideButtons.forEach((item) => {
      const isActive = item.dataset.guide === activeGuide;
      item.classList.toggle("is-active", isActive);
      item.setAttribute("aria-pressed", String(isActive));
    });
    twinPrompts.querySelectorAll?.("[data-prompt-id]").forEach((item) => {
      item.classList.remove("is-active");
      item.setAttribute("aria-pressed", "false");
    });
  });
});

twinPrompts?.addEventListener("click", (event) => {
  const button = event.target.closest("[data-prompt-id]");
  if (!button) return;

  activeTwinPrompt = button.dataset.promptId;
  renderTwin(document.body.dataset.langActive || "zh");
});

noteForm?.addEventListener("submit", (event) => {
  event.preventDefault();

  const lang = document.body.dataset.langActive || "zh";
  const noteCopy = copy[lang].note;
  const formData = new FormData(noteForm);
  const name = String(formData.get("name") || "").trim();
  const contact = String(formData.get("contact") || "").trim();
  const reason = String(formData.get("reason") || "").trim();
  const message = String(formData.get("message") || "").trim();

  noteFeedback.classList.remove("is-error");

  if (!message) {
    noteFeedback.textContent = noteCopy.emptyMessage;
    noteFeedback.classList.add("is-error");
    return;
  }

  const body = [
    `${noteCopy.bodyLabels.name}: ${name || "-"}`,
    `${noteCopy.bodyLabels.contact}: ${contact || "-"}`,
    `${noteCopy.bodyLabels.reason}: ${reason || "-"}`,
    "",
    `${noteCopy.bodyLabels.message}:`,
    message,
    "",
    noteCopy.fallback
  ].join("\n");

  const mailto = `mailto:yaoyaosocial01@gmail.com?subject=${encodeURIComponent(noteCopy.subject)}&body=${encodeURIComponent(body)}`;
  window.location.href = mailto;
  noteFeedback.textContent = noteCopy.success;
});

